package day19_ForLoop;

public class SumOfEvenOdd {
    public static void main(String[] args) {

        for(int i=0;i<=100;i+=2){
            System.out.printf(i+" ");
        }
        for(int i=97; i<=122; i++){
            char ch = (char)i;
            System.out.print(ch+" ");
        }
    }
}
