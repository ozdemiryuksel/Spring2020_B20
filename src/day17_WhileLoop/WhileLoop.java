package day17_WhileLoop;

public class WhileLoop {
    public static void main(String[] args) {
        for(int i=1;i<=10;i++){
            System.out.print(i+" ");
        }
        System.out.println("\n---------------");
        int num=1;
        while(num<=10){
            System.out.print(num+" ");
            num++;
        }
    }
}
